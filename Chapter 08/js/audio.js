var audioContr1 = document.getElementById("audioPlayer");
var center1 = document.getElementById("zone1");

audioContr1.onplay = function () {

    if (audioContr1.play){
        
        center1.classList.add("audioFixe");
        audioContr1.loop = true;

    }


}

audioContr1.onpause = function () {
            
    if(audioContr1.pause){

        center1.classList.remove("audioFixe");
        audioContr1.loop = false;
    }

}

